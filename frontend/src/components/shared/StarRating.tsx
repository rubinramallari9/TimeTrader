"use client";

import { useState } from "react";

interface ReadOnlyProps {
  rating: number;
  count?: number;
  interactive?: false;
}

interface InteractiveProps {
  rating: number;
  count?: number;
  interactive: true;
  onChange: (rating: number) => void;
}

type Props = ReadOnlyProps | InteractiveProps;

const StarPath = () => (
  <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
);

export default function StarRating(props: Props) {
  const [hovered, setHovered] = useState(0);

  // Interactive mode — whole stars only
  if (props.interactive) {
    const display = hovered || props.rating;
    return (
      <span className="inline-flex items-center gap-1">
        {[1, 2, 3, 4, 5].map((star) => (
          <svg
            key={star}
            className={`w-5 h-5 cursor-pointer transition-colors ${star <= display ? "text-amber-400" : "text-[#EDE9E3]"}`}
            viewBox="0 0 20 20"
            fill="currentColor"
            onMouseEnter={() => setHovered(star)}
            onMouseLeave={() => setHovered(0)}
            onClick={() => props.onChange(star)}
          >
            <StarPath />
          </svg>
        ))}
        {props.count !== undefined && (
          <span className="text-xs text-[#9E9585] ml-0.5">({props.count})</span>
        )}
      </span>
    );
  }

  // Read-only mode — supports decimals / half stars
  const rating = props.rating ?? 0;

  return (
    <span className="inline-flex items-center gap-0.5">
      {[1, 2, 3, 4, 5].map((star) => {
        // How much of this star position is filled (0–1)
        const fill = Math.min(1, Math.max(0, rating - (star - 1)));
        const pct = Math.round(fill * 100);

        return (
          <span key={star} className="relative inline-block w-4 h-4">
            {/* Empty (gray) star — base layer */}
            <svg
              viewBox="0 0 20 20"
              fill="currentColor"
              className="w-4 h-4 text-[#EDE9E3] absolute inset-0"
            >
              <StarPath />
            </svg>

            {/* Gold fill — clipped to the fill percentage */}
            {pct > 0 && (
              <span
                className="absolute inset-0 overflow-hidden"
                style={{ width: `${pct}%` }}
              >
                <svg
                  viewBox="0 0 20 20"
                  fill="currentColor"
                  className="w-4 h-4 text-amber-400"
                >
                  <StarPath />
                </svg>
              </span>
            )}
          </span>
        );
      })}

      {props.count !== undefined && (
        <span className="text-xs text-[#9E9585] ml-1">
          {rating > 0 ? `${rating.toFixed(1)} ` : ""}({props.count})
        </span>
      )}
    </span>
  );
}
